package com.codinginflow.trashapp.model;

public class PesananItem {
    private String mText1, mText2, mText3;

    public PesananItem(String mText1, String mText2, String mText3) {
        this.mText1 = mText1;
        this.mText2 = mText2;
        this.mText3 = mText3;
    }

    public String getmText3() {
        return mText3;
    }

    public void setmText3(String mText3) {
        this.mText3 = mText3;
    }

    public String getmText2() {
        return mText2;
    }

    public void setmText2(String mText2) {
        this.mText2 = mText2;
    }

    public String getmText1() {
        return mText1;
    }

    public void setmText1(String mText1) {
        this.mText1 = mText1;
    }
}
